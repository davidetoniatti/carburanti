export function t(key, params = {}) {
  let text = translations[state.lang][key] || key;
  Object.keys(params).forEach(p => {
    text = text.replace(`{${p}}`, params[p]);
  });
  return text;
}

export function hasLocale(lang) {
  return lang in translations;
}
